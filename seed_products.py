def main():
    # Build SQL
    lines = ["BEGIN;"]
    lines.append("DELETE FROM public.global_products;")
    for cat, products in PRODUCTS.items():
        for name in products:
            safe_name = name.replace("'", "''")
            safe_cat = cat.replace("'", "''")
            lines.append(
                f"INSERT INTO public.global_products (name, category) "
                f"VALUES ('{safe_name}', '{safe_cat}');"
            )
    lines.append("COMMIT;")

    sql = "\n".join(lines)

    # Write to temp file and SCP to VPS
    with tempfile.NamedTemporaryFile(mode='w', suffix='.sql', delete=False,
                                     encoding='utf-8', newline='\n') as f:
        f.write(sql)
        tmp_path = f.name

    vps = "root@72.62.119.187"
    key = os.path.expanduser("~/.ssh/id_ed25519")
    remote_path = "/tmp/seed_products.sql"

    subprocess.run(["scp", "-i", key, tmp_path, f"{vps}:{remote_path}"], check=True)
    os.unlink(tmp_path)

    result = subprocess.run(
        ["ssh", "-i", key, vps,
         f"cd /app/supabase && docker compose exec -T db psql -U postgres < {remote_path}"],
        capture_output=True, text=True
    )
    print(result.stdout)
    if result.stderr:
        print("STDERR:", result.stderr)

    # Show stats
    stats = subprocess.run(
        ["ssh", "-i", key, vps,
         "cd /app/supabase && docker compose exec -T db psql -U postgres -c "
         "\"SELECT category, count(*) FROM public.global_products GROUP BY category ORDER BY category;\" "
         "-c \"SELECT count(*) AS total FROM public.global_products;\""],
        capture_output=True, text=True
    )
    print(stats.stdout)

    total_cats = len(PRODUCTS)
    total_prods = sum(len(v) for v in PRODUCTS.values())
    max_cat = max(PRODUCTS.items(), key=lambda x: len(x[1]))
    print(f"\nPrepared: {total_cats} categories, {total_prods} products")
    print(f"Max in one category: {max_cat[0]} = {len(max_cat[1])}")

if __name__ == "__main__":
    main()
