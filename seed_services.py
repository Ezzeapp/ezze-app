def q(s): return s.replace("'", "''")


def build_sql():
    lines = ["DELETE FROM public.global_services WHERE TRUE;"]
    for category, services in SERVICES.items():
        for name in services:
            lines.append(
                f"INSERT INTO public.global_services (name, category) "
                f"VALUES ('{q(name)}', '{q(category)}');"
            )
    lines.append("SELECT category, COUNT(*) FROM public.global_services GROUP BY category ORDER BY category;")
    lines.append("SELECT COUNT(*) AS total FROM public.global_services;")
    return '\n'.join(lines)


def main():
    sql = build_sql()
    total = sum(len(v) for v in SERVICES.values())
    cats = len(SERVICES)
    print(f"Prepared: {cats} categories, {total} services")
    print(f"Max in one category: Волосы = {len(SERVICES['Волосы'])}")

    tmp = Path('/tmp/seed_services.sql')
    tmp.write_text(sql, encoding='utf-8')
    subprocess.run(['scp', '-i', str(Path.home() / '.ssh' / 'id_ed25519'),
                    str(tmp), 'root@72.62.119.187:/tmp/seed_services.sql'], check=True)
    cmd = SSH + ['docker exec -i supabase-db psql -U postgres < /tmp/seed_services.sql']
    r = subprocess.run(cmd, capture_output=True, text=True, encoding='utf-8')
    out = r.stdout
    if 'ERROR' in r.stderr:
        print('ERR:', r.stderr)
        sys.exit(1)
    # Show summary (last lines)
    lines = [l for l in out.splitlines() if l.strip() and 'INSERT' not in l]
    print('\n'.join(lines[-40:]))
    print("Done.")


if __name__ == '__main__':
    main()
