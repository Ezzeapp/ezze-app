-- 1a. "Химчистка мебели" → "Мягкая мебель"
UPDATE global_services
   SET category = 'Мягкая мебель',
       order_type = 'furniture'
 WHERE product = 'cleaning'
   AND category = 'Химчистка мебели';

-- 1b. Кожаный диван — это мебель, не одежда
UPDATE global_services
   SET category = 'Мягкая мебель',
       order_type = 'furniture'
 WHERE product = 'cleaning'
   AND name = 'Чистка кожаного дивана';

-- 1c. Кожаная сумка — аксессуар, не одежда из кожи
UPDATE global_services
   SET category = 'Аксессуары'
 WHERE product = 'cleaning'
   AND name = 'Чистка кожаной сумки';

-- ═══════════════════════════════════════════════════════════════
-- 2. ПЕРЕНОС СПЕЦУСЛУГ В ОТДЕЛЬНЫЙ ORDER_TYPE 'extras'
-- ═══════════════════════════════════════════════════════════════

UPDATE global_services
   SET order_type = 'extras'
 WHERE product = 'cleaning'
   AND category = 'Спецуслуги';

